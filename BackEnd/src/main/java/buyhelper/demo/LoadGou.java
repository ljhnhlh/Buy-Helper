package buyhelper.demo;

public class LoadGou {
    private int did;
    private String avatarUrl;
    private String nickName;
    private int stars;
    private String destination;
    private String description;
    private String last_for_time;

    public int getDid() {
        return did;
    }

    public void setDid(int did) {
        this.did = did;
    }

    public String getAvatarUrl() {
        return avatarUrl;
    }

    public void setAvatarUrl(String avatarUrl) {
        this.avatarUrl = avatarUrl;
    }

    public String getNickname() {
        return nickName;
    }

    public void setNickname(String nickname) {
        this.nickName = nickname;
    }

    public int getStars() {
        return stars;
    }

    public void setStars(int stars) {
        this.stars = stars;
    }

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLast_for_time() {
        return last_for_time;
    }

    public void setLast_for_time(String last_for_time) {
        this.last_for_time = last_for_time;
    }
}
